          To date we have assessed 10 people, 7 of whom we have been able to help, the majority of whom are children. Of the 3 adults we have assessed, we have been unable to help 2 people
          due to the limitations of our current printers, which are too small to print adult-sized prosthetics.<br/>
          In order to help these people and grow our impact, we’re currently raising funds through Generosity. A priority for us is to buy a larger 3D printer so that we are able to help more adults
          who have been affected by landmines.<br/>
