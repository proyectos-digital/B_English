          In October 2016, a significant milestone was reached in the country’s ongoing quest to restore normality to the rural regions most affected by the country’s 52-year conflict: President
          Juan Manuel Santos declared three additional municipalities free of landmines, bringing the total across the country to eight.<br/>
          The devastating impact of landmines in Colombia is relatively well-reported, but unsolved: between 1990 and 2014, over 11,400 became a victim of landmines. More than 1,120 of these
          victims are children.<br/>
          Despite the valiant efforts of mine clearance organizations such as The Halo Trust, Decontamina Colombia and the UN Mine Action Service, in the first half of 2017 an additional 16 people
          became victims of landmines, 12 of whom were civilians.<br/>
          Our home is Antioquia, the province with the greatest number of landmines in Colombia, the country with the second-largest number of landmine victims in the world after Afghanistan.<br/>
          In order to help victims, we have established a charity with an innovative solution to help those who have been injured by these horrible artifacts to rebuild their lives, regain their
          independence and return to normality.<br/>
          As part of the global Enabling the Future network, at Enable Medellin we’re harnessing the power of 3D printing to provide people in need in Colombia with free, personalized 3D printed
          prosthetic arms and devices.<br/>
          Traditionally a prosthetic arm can cost between US$5000 to US$100k, an obviously prohibitive cost to many Colombians. By using 3D printing technology, we not only save on costs, but
          are able to produce quality, personalized prosthetics in just 2 weeks.<br/>
          We can work with people missing any part of their arm from the elbow, down, e.g. fingers, hand, or the entire forearm. All new hands and arms are fully functional mechanical prosthetics,
          which we always provide to the recipient free of charge.<br/>
          We’re just getting started and have assembled a great team of local and international volunteers which includes doctors, bio engineers, students, artists, digital nomads and the general
