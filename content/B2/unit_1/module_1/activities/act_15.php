<script>
  let 
        gameComp_btnClose = document.querySelector('.c-gameCompletePhrase--button-Close')
      , gameComp_btnInit = document.querySelector('.c-gameCompletePhrase--button-Init')
      , gameComp_btnInstruction = document.querySelector('.c-gameCompletePhrase--button-Instructions')
      , gameComp_FeedBack = document.querySelector('.c-gameCompletePhrase--FeedBack')
      , gameComp_Init = document.querySelector('.c-gameCompletePhrase--Init')
      , gameCompletePhrase_funInit = (options= 'init', action) => {
          if( options == 'init' ){
              if( gameComp_Init.classList.contains('is-hidden') ){
                  console.log(`gameComp_Init ${gameComp_Init.classList.contains('is-hidden')}`)
                  gameComp_Init.classList.remove('is-hidden')
              } else {
                  gameComp_Init.classList.add('is-hidden')
              }
          }
          if( options == 'instruction' ){
              if( gameComp_FeedBack.classList.contains('is-visible') ){
                  console.log(`gameComp_FeedBack ${gameComp_FeedBack.classList.contains('is-hidden')}`)
                  gameComp_FeedBack.classList.remove('is-visible')
  
              } else {
                  gameComp_FeedBack.classList.add('is-visible')
              }
          }
      }
  ;
  
  
  gameComp_btnInit.addEventListener('click', function(e){
      gameCompletePhrase_funInit();
      avvia();
  })
  gameComp_btnInstruction.addEventListener('click', function(e){
      gameCompletePhrase_funInit('instruction');
  })
  gameComp_btnClose.addEventListener('click', function(e){
      gameCompletePhrase_funInit('instruction');
  })
  
  
  var hora = 0, minuti = 0, secondi = 0, decimi = 0;
  var vis = "";
  var stop = true;
  var game2=2;
  var game1=1;
  var contador=0;
  
  function avvia() {
      contador++;
      if(stop == true) {
          stop = false;
          cronometro();
      
          if(contador > 1){
              game2++;
              game1++;
          }
  
      }
  }
  
  function cronometro() {
  
      if(stop == false) {
          decimi++;
          if(decimi > 9) {
              decimi = 0;
              secondi++;
          }
          if(secondi > 59) {
              secondi = 0;
              minuti++;
          }
          if(minuti > 59) {
              minuti = 0;
              hora++;
          }
          mostra();
          setTimeout("cronometro()", 100);
  
          if(secondi==59){
                      avvia()
                      hora = 0, minuti = 0, secondi = 0, decimi = 0;
                      vis = "";
                      stop = true;
          }
  
      }
  } 
  
  function mostra() {
      if(hora < 10) vis = ""; else vis = hora;
      if(minuti < 10) vis = vis + "0";
      vis = vis + minuti + ":";
      if(secondi < 10) vis = vis + "0";
      vis = secondi;
  
      $('.c-gameCompletePhrase--Timer').val(vis);
  }
</script>